# Исходный список для сортировки
numbers = [64, 34, 25, 12, 90, 22, 11, 100]

n = len(numbers)

# Проходим по всем элементам списка
for i in range(n - 1):
    # Оптимизация: если не было обменов, значит список уже отсортирован
    swapped = False
    # Проходим по списку, оставляя последний элемент каждого цикла на месте
    for j in range(n - i - 1):
        # Если текущий элемент больше следующего, меняем их местами
        if numbers[j] > numbers[j + 1]:
            numbers[j], numbers[j + 1] = numbers[j + 1], numbers[j]
            swapped = True
    # Если не было обменов, выходим из цикла
    if not swapped:
        break

# Вывод отсортированного списка
print("Отсортированный список:", numbers)